void dsp_g2_test()
{
	u8 temp[50]={0};//ÁÙÊ±×Ö·û´®´æ´¢
  u16 i=0;
  u32 Fs=6400;
  for(i=0;i<NPT;i++)
  {
    s[i].real = 32000 * sin(PI2*i*(50.0f/Fs));
    s[i].real+= 16000 * sin(PI2*i*(550.0f/Fs));
    s[i].real+= 9000 * sin(PI2*i*(1150.0f/Fs));
    s[i].imag=0;
  }
  FFT(s,NPT);
  for(i=0;i<NPT/2;i++)
  {
    result[i] = sqrt(s[i].real * s[i].real + s[i].imag * s[i].imag)*2/NPT;
    //if(result[i] > 10)
    //printf("%4d,%4d,%ld\n",i,(u16)((double)i*Fs/NPT),(u32)result[i]);
		sprintf(temp,"%d Times Result:%.2f\r\n\0",i,result[i]);
		Uart5_Send_String(temp);
		delay_ms(200);
		
  }
  
}
